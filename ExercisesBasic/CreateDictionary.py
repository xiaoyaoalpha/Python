################################################################################################
# 构造字典，方法一
################################################################################################

names = ['Michael', 'Bob', 'Tracy']
scores = [95, 75, 85]
L = []
for i in range(len(names)):
    L.append((names[i],scores[i]))
d = dict(L)
print(d)


